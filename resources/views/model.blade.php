		<h2>品牌资产质量——利益相关者评价模型维度设计考虑因素</h2>
		<p>
			<dl>
				<dt>1、品牌价值来源</dt>
				品牌评价的维度设计涵盖了由品牌功能衍生出的所有经济利益。如：
				<dd>1）创造更好的认知和交流信息的连接，由此产生沟通效率促进品牌经营的获利性。</dd>
				<dd>2）促进产品和服务的差异化，带来客户购买行为偏好的建立，最终产生财务增长。</dd>
				<dd>3）有利于客户的获取和保留，从而增加业务的可持续性，保证未来需求，从而减小公司的经营风险。</dd>
				<dt>2、品牌价值驱动因素</dt>
				<dd>1）全面评价重要的利益相关者通过对品牌的感知、竞品的比较，以及对品牌特定风险的评估，决定品牌未来的成功，从而保持或增加品牌价值。</dd>
				<dd>2）强化品牌作用和产生品牌忠诚的基础，从而提高继续购买和品牌产生溢价的能力。</dd>
				<dd>3）没有全面和努力地评价重要的利益相关者对品牌的感知以及与竞争品牌的比较，对品牌价值和品牌特定风险的评估是没有意义的。</dd>
				<dt>3、品牌关联性</dt>
				<dd>1）每一项品牌评价应考虑特定市场和行业环境对品牌的影响，确定由品牌施加给市场目标群体的购买决策的影响。</dd>
				<dd>2）每一项品牌评价应考虑行业关联性并与公司特点相结合，将其影响转变成品牌对总价值的贡献度。</dd>
				<dd>3）评价品牌变化在所处市场或细分行业中关联性的增长或下降。</dd>
				<dt>4、品牌强度</dt>
				<dd>1）品牌强度的测量包括认知度、感知特征、知识、态度和忠诚度。</dd>
				<dd>2）对品牌在重要的利益相关者群体中的强度进行分析，估计将来的销售数量、收益和风险水平。</dd>
				<dd>3）评价品牌强度受消费者行为和趋势的变化、品牌投资、竞争活动以及商标强制执行保护计划的影响程度。</dd>
				<dd>4）提供的品牌强度数据的质量和数量在品牌之间的显著差别。</dd>
				<dt>5、法律保护</dt>
				<dd>1）法律保护是影响品牌价值的一个因素，因此评估在每个相关管辖区内为品牌提供的法律保护，是品牌评价一个重要组成部分。</dd>
				<dd>2）确定品牌价值只属于合法持有人的合法权利。</dd>
				<dd>3）评估品牌保护的合法权利与法律体系之间存在的差别。</dd>
			</dl>
		</p>
