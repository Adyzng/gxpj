		<h2>品牌资产质量——利益相关者评价模型的使用</h2>
		<p>
			品牌资产质量-利益相关者评价模型的评价方法采用量表调查法，它是一种标准化的等级量表，其优点是调查项目设计严格、调查问题明确、被调查对象的意向选择比较规范、计量方法统一、调查结论经统计分析方法分析、调查结果便于计量、便于横向和纵向比较分析。
			评价方式包括专家测评、利益相关者测评、员工自测评等。
