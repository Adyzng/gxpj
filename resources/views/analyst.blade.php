<div class="container">
	<div class="analyst_left">
		<h2>品牌分析师的意义</h2>
		<p>
			随着互联网经济在全球的迅猛发展，品牌管理已经跨越了形象和营销的范畴，向互联网、金融、品牌化组织等领域发展，品牌创建与管理工作也日趋复杂化和专业化，针对这一趋势，品牌分析师这一专业角色应运而生。著名的Interbrand公司、国际品牌爱马仕集团、阿里巴巴集团等都设有品牌分析师这一职位。面对这一新兴的专业市场需求，北京国信品牌评价科学研究院在领军人物杨曦沦的带领下，以深厚的品牌理论积淀和大量的品牌实践，开设了品牌分析师认证课，并设计了品牌资产质量-利益相关者评价模型，为品牌分析师配置了强大的品牌分析工具。
		</p>
		<br>
		<h2>品牌分析师的职责</h2>
		<p>
			品牌分析师的工作需要与市场推广、创意、销售和运营等各部门密切配合，从概念阶段开始，贯穿整个品牌生命周期，完成全面、完整的产品开发计划和品牌价值分析，包括品牌规划、组织产品发布，其目的是在销售额增加和利润最大化的同时，实现品牌价值的保值和增值。品牌分析师的工作涉及多个方面，通常包括：
		</p>
		<ol class="decimal_style">
			<li>
				对公司产品及品牌价值及用户资源充分挖掘，发现互联网新模式，协同推进项目整体规划。
			</li>
			<li>
				熟练掌握品牌分析评价工具的原理及使用方法，组织实施市场、技术和竞争品牌的评估与分析，分析品牌各维度数据，对业内相关品牌进行监测，帮助企业客观了解品牌资产质量状况，为品牌战略、战术决策提供依据。
			</li>
			<li>
				负责产品及品牌分析工作，包括业务报表、行业研究、消费者研究和产品调研等工作。研究和识别公司需要哪些附加产品或品牌强化，协助产品定价、产品成本和毛利润评估。
			</li>
			<li>
				主持或协助制定新产品开发及产品升级计划，与相关部门共同组织实施，保证项目可交付成果在预算之内按期交付；为公司维护并保存标准产品开发过程档案。
			</li>
			<li>
				协助制订、实施品牌营销组合方案、传播推广计划。
			</li>
			<li>
				维持消费者访问并评估关键性的市场营销方案，如产品标准、产品规范要求形式、包装形式、客户体验等等。
			</li>
		</ol>
		<br>
		<h2>品牌分析师的任职资格</h2>
		<ol class="decimal_style">
			<li>
				大学学历，相应教育背景，并有特定的产品、品牌管理经验，对品牌具有持续的热情。
			</li>
			<li>
				对品牌专业知识、体系、方法、工具有清晰的认知，并具备较强的综合技术分析能力、表达能力和解决问题的能力。
			</li>
			<li>
				具有良好的信息收集和处理能力，熟练掌握调研方法及各种统计分析软件，能够做基本的数据挖掘。
			</li>
			<li>
				具有在最后期限驱动环境下优先处理和执行任务的能力。
			</li>
		</ol>
		<br>
		<h2>品牌分析师等级</h2>
		<ol class="decimal_style">
			<li>
				注册品牌分析师：符合品牌分析师的任职条件，并且完成品牌分析师认证课程。
			</li>
			<li>
				高级品牌分析师：
				<dl>
					<dd>
					1）取得注册品牌分析师认证，且具有3年以上品牌分析师相关工作经验，并发表过品牌分析评价方面的研究文章或主持过品牌案例。
					</dd>
					<dd>
					2）取得资产评估师、数据分析师、证券分析师、商标律师、品牌管理师等认证资质者，并发表过品牌分析评价方面的研究文章或主持过品牌案例。
					</dd>
					<dd>
					3）MBA\EMBA、研究生学历，并发表过品牌分析评价方面的研究文章或主持过品牌案例。
					</dd>
				</dl>
			</li>
			<li>
				首席品牌分析师：取得高级品牌分析师资格，对某一行业品牌有深入研究并取得重要成果，被同行广泛认可。
			</li>
		</ol>
